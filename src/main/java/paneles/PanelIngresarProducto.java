    private void btnRegistrarTiendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarTiendaActionPerformed

        Producto entidadAux = new Producto(txtCodigoProducto.getText(), txtCantidad.getText(), txtPrecio.getText(), txtNombreProducto.getText(), 
                txtFabricante.getText(), txtDescripcion.getText(), txtGarantia.getText(), String.valueOf(txtTienda.getSelectedItem()));
        //Revisamos los campos obligatorios, si están llenos o vacíos
        if(entidadAux.getCodigo_producto().isEmpty() || entidadAux.getNombre_producto().isEmpty() || entidadAux.getFabricante().isEmpty() || 
                entidadAux.getCantidad().isEmpty() || entidadAux.getPrecio().isEmpty()){
            JOptionPane.showMessageDialog(null, "Ingrese todos los datos obligatorios (*)");
        }else{
            //instanciamos y agregamos todos los datos
            SubStringDatos auxiliarSubDatos = new SubStringDatos("Producto (");
            auxiliarSubDatos.agregarSubStringYDato(entidadAux.getCodigo_producto(), "codigo_producto");
            auxiliarSubDatos.agregarSubStringYDato(entidadAux.getCantidad(), ", cantidad");
            auxiliarSubDatos.agregarSubStringYDato(entidadAux.getPrecio(), ", precio");
            auxiliarSubDatos.agregarSubStringYDato(entidadAux.getNombre_producto(), ", nombre_producto");
            auxiliarSubDatos.agregarSubStringYDato(entidadAux.getFabricante(), ", fabricante");
            auxiliarSubDatos.agregarSubStringYDato(entidadAux.getDescripcion(), ", descripcion");
            auxiliarSubDatos.agregarSubStringYDato(entidadAux.getGarantia(), ", garantia");
            auxiliarSubDatos.agregarSubStringYDato(entidadAux.getCodigo_tienda(), ", codigo_tienda");

            //REGISTRAMOS EL EMPLEADO
            RegistroDatos regAux = new RegistroProducto(auxiliarSubDatos.getSubOrden());//Le agregamos la suborden
            regAux.registrarDatos(auxiliarSubDatos.getDatos());//creamos el registro con los datos

        }

    }//GEN-LAST:event_btnRegistrarTiendaActionPerformed

